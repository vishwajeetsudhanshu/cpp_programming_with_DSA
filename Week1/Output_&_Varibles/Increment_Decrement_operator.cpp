#include<iostream>
using namespace std;
int main(){
    int x=5;
    x++; // post increment -> x-x+1
    cout<<x<<endl;
    x--; // post decrement
    cout<<x<<endl;
    ++x; //pre incement
    cout<<x<<endl;
    --x; //pre decrement
    cout<<x<<endl;
    x=8;
    cout<<--x<<endl;
    cout<<x--<<endl;
    cout<<x;
    return 0;

}

